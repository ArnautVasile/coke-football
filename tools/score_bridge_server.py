def main() -> None:
    args = parse_args()
    hits_csv = Path(args.hits_csv)
    db_path = Path(args.db)
    store = ScoreBridgeStore(db_path=db_path, hits_csv=hits_csv)

    stop_event = threading.Event()
    poller = threading.Thread(
        target=poll_loop,
        args=(store, float(args.poll_seconds), stop_event),
        daemon=True,
    )
    poller.start()

    handler = create_handler(store, int(args.history_limit))
    server = ThreadingHTTPServer((str(args.host), int(args.port)), handler)
    print(f"[ScoreBridge] hits_csv={hits_csv.resolve()}")
    print(f"[ScoreBridge] db={db_path.resolve()}")
    print(f"[ScoreBridge] ui=http://{args.host}:{args.port}/")
    print("[ScoreBridge] Unity poll endpoint: GET /api/state")

    try:
        server.serve_forever()
    except KeyboardInterrupt:
        pass
    finally:
        stop_event.set()
        server.server_close()


if __name__ == "__main__":
    main()
